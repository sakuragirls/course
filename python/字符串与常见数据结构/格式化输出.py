# %的用法
"""
1.整数输出

%o =八进制
%d =十进制
%x =十六进制

"""

print('%d' % 20) #将20转换成十进制
print('%0' % 20) #将20转换成八进制


"""
2.浮点数输出
%.f :保留6位有效数字
%.3f:保留3位小数点

%.e:保留6位有效数字,以指数形式输出
%.3e:保留3位小数点，以指数形式输出
"""

print('%.2f' % 3.1415926)


"""
3.字符串输出
%s
%.2s:截取2位字符串
%2s:右对齐，占位符2位
%2.2s:占位符2位，截取2位字符串
"""

print('%.2s' % 'hello world') #结果为he


age=18
history='liming'
print('%s已经%d岁了' % (history,age))

